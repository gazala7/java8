package datatype;

public class IntegerLiterals {
	public static void main(String[] args) {
		int a=0B100;
		System.out.println(a);
		int b=045;
		System.out.println(b);
		int c=0x44;
		System.out.println(c);
		int d=100000000;
		int e=1_00_00_00;
		long f=62_00_20_37_34L;
		float g=99.99_99_99_99F;
		System.out.println("'gazala'");
		System.out.println("\'Gazala\'");
		System.out.println("\"gazala\"");
		System.out.println("*");
		System.out.println();
		
	}
}
