package operator;
//10+20= 30(addition)
//10+20= 1020(concatination) means not addition, it means append
public class StringConcatination {
public static void main(String[] args) {
	System.out.println(10+20);
	System.out.println(""+10+20);
	System.out.println(10+20+""+30+40);
	System.out.println(10+20+30+"Gazala"+50+60);
	System.out.println("10+20");
	System.out.println("5060+5060");
	System.out.println(45+19+31+46+01+05+"oi+b2+l5+np");
	System.out.println(21+34+"30+40");
	System.out.println(45+19+31+46+01+05+"oi+b2+01=03-21*0/l5+np");
	System.out.println(32-64*43/0);
}
}
